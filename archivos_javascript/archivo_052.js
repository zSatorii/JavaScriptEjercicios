// Igual que (==)
5 == "5"; // true (compara valor, no tipo)

// Estrictamente igual (===)
5 === "5"; // false (compara valor y tipo)

// No igual (!=)
5 != 8; // true

// Estrictamente no igual (!==)
5 !== "5"; // true

// Mayor que (>)
10 > 5; // true

// Menor que (<)
3 < 7; // true

// Mayor o igual que (>=)
5 >= 5; // true

// Menor o igual que (<=)
4 <= 3; // false